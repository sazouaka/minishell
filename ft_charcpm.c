int ft_charcmp(char c, char *str)
{
    int i;

    i = 0;
    while (str[i] != '\0')
    {
        if (str[i] == ' ' || str[i] == '\t')
            return (0);
        i++;
    }
    if (str[i] == '\0')
        return (0);
    else
        return (1);
}
